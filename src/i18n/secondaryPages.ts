function simpleGroup(
  group: string,
  rows: [slug: string, cnTitle: string, twTitle: string, cnDescription: string, twDescription: string][],
): SecondaryPage[] {
  return rows.map(([slug, cnTitle, twTitle, cnDescription, twDescription]) => page(
    `${group}/${slug}`,
    group,
    cnTitle,
    twTitle,
    cnDescription,
    twDescription,
    [
      cnDescription,
      `该页面属于${groupLabels['zh-cn'][group] ?? '研究院'}栏目，用于说明相关制度、项目、资源或申请流程的核心信息。`,
      '如需提交申请或开展合作，请通过研究院联系页面或相应申请入口提交材料。',
    ],
    [
      twDescription,
      `該頁面屬於${groupLabels['zh-tw'][group] ?? '研究院'}欄目，用於說明相關制度、項目、資源或申請流程的核心資訊。`,
      '如需提交申請或開展合作，請通過研究院聯絡頁面或相應申請入口提交材料。',
    ],
  ));
}

export function getSecondaryPage(lang: Lang, slug: string): PageCopy | undefined {
  const entry = secondaryPages.find((p) => p.slug === slug);
  if (!entry) return undefined;
  return lang === 'zh-tw' ? entry.zhTw : entry.zhCn;
}

export function getSecondaryStaticPaths(lang: 'zh-cn' | 'zh-tw') {
  return secondaryPages.map((entry) => ({
    params: { slug: entry.slug },
    props: { page: lang === 'zh-tw' ? entry.zhTw : entry.zhCn, slug: entry.slug },
  }));
}

