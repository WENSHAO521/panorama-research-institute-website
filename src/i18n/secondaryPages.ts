import type { Lang } from './utils';

type PageCopy = {
  title: string;
  description: string;
  eyebrow: string;
  sections: { title: string; body: string }[];
};

export type SecondaryPage = {
  slug: string;
  group: string;
  zhCn: PageCopy;
  zhTw: PageCopy;
};

function page(
  slug: string,
  group: string,
  cnTitle: string,
  twTitle: string,
  cnDescription: string,
  twDescription: string,
  cnSections: string[],
  twSections: string[],
): SecondaryPage {
  return {
    slug,
    group,
    zhCn: {
      title: cnTitle,
      description: cnDescription,
      eyebrow: groupLabels['zh-cn'][group] ?? '页面',
      sections: cnSections.map((body, i) => ({
        title: sectionLabels['zh-cn'][i] ?? `说明 ${i + 1}`,
        body,
      })),
    },
    zhTw: {
      title: twTitle,
      description: twDescription,
      eyebrow: groupLabels['zh-tw'][group] ?? '頁面',
      sections: twSections.map((body, i) => ({
        title: sectionLabels['zh-tw'][i] ?? `說明 ${i + 1}`,
        body,
      })),
    },
  };
}

